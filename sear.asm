ASSUME CS:CODE,DS:DATA

DATA SEGMENT
STRING1 DB 11H,12H,13H,14H,15H
MSG1 DB "FOUND$"
MSG2 DB "NOT FOUND$"
SE DB 13H
DATA ENDS


CODE SEGMENT
START:
MOV AX,DATA
MOV DS,AX
LEA SI,STRING1
MOV CX,5H
MOV AL,SE
LOOP1:
CMP AL,[SI]
JZ FOUND
INC SI
DEC CX
JNZ LOOP1
MOV AH,09H
LEA DX,MSG2
INT 21H
JMP END1

FOUND:
MOV AH,09H
LEA DX,MSG1
INT 21H

END1:
MOV AH,4CH
INT 21H
CODE ENDS
END START
